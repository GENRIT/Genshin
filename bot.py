@bot.message_handler(func=lambda message: True)
def handle_message(message):
    user_id = message.from_user.id
    user_count.add(user_id)

    bot.send_chat_action(message.chat.id, 'typing')

    user_text = message.text.lower()

    if user_id in user_modes:
        mode = user_modes[user_id]
        if mode == "programmer":
            response = get_gemini_response(user_text, PROGRAMMER_PROMPT)
        elif mode == "designer":
            response = get_gemini_response(user_text, DESIGNER_PROMPT)
        elif mode == "arbitrage":
            response = get_gemini_response(user_text, ARBITRAGE_PROMPT)
        elif mode == "psychologist":
            response = get_gemini_response(user_text, PSYCHOLOGIST_PROMPT)
        elif mode == "trainer":
            response = get_gemini_response(user_text, TRAINER_PROMPT)
        elif mode == "cold":
            response = get_gemini_response(user_text, COLD_PROMPT)
        elif mode == "economist":
            response = get_gemini_response(user_text, ECONOMIST_PROMPT)
        bot.reply_to(message, response)
    else:
        bot.reply_to(message, "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤—ã–±–µ—Ä–∏ —Ä–µ–∂–∏–º —Ä–∞–±–æ—Ç—ã –ø–µ—Ä–µ–¥ –Ω–∞—á–∞–ª–æ–º.")

def get_gemini_response(user_input, prompt):
    response_text = ""
    try:
        data = {
            "prompt": f"{prompt}\n–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å: {user_input}\n–ö–∞–º–∏–ª–ª–∞:",
            "temperature": 0.6
        }
        headers = {
            "Content-Type": "application/json",
            "Authorization": f"Bearer {GEMINI_API_KEY}"
        }
        response = requests.post(GEMINI_API_URL, headers=headers, json=data)
        response.raise_for_status()
        gemini_data = response.json()
        response_text = gemini_data['candidates'][0]['output']
    except requests.RequestException as e:
        logging.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –≤—ã–∑–æ–≤–µ Gemini API: {e}")
        response_text = "–ò–∑–≤–∏–Ω–∏—Ç–µ, –ø—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –≤–∞—à–µ–≥–æ –∑–∞–ø—Ä–æ—Å–∞."
    return response_text

